<script>
  var chips = document.querySelectorAll('.chip');
  var cards = document.querySelectorAll('.card');
  chips.forEach(function(chip) {
    chip.addEventListener('click', function() {
      chips.forEach(function(c) { c.classList.remove('active'); });
      chip.classList.add('active');
      var f = chip.dataset.filter;
      cards.forEach(function(card) {
        var show = (f === 'all' || card.dataset.cat === f);
        card.style.display = show ? '' : 'none';
      });
    });
  });

  // Mobile nav
  var burgerBtn     = document.getElementById('burgerBtn');
  var mobileNav     = document.getElementById('mobileNav');
  var mobileNavClose = document.getElementById('mobileNavClose');

  function openNav() {
    mobileNav.classList.add('open');
    mobileNav.setAttribute('aria-hidden', 'false');
    burgerBtn.setAttribute('aria-expanded', 'true');
    burgerBtn.classList.add('is-open');
    document.body.style.overflow = 'hidden';
  }
  function closeNav() {
    mobileNav.classList.remove('open');
    mobileNav.setAttribute('aria-hidden', 'true');
    burgerBtn.setAttribute('aria-expanded', 'false');
    burgerBtn.classList.remove('is-open');
    document.body.style.overflow = '';
  }

  burgerBtn.addEventListener('click', function() {
    mobileNav.classList.contains('open') ? closeNav() : openNav();
  });
  mobileNavClose.addEventListener('click', closeNav);

  // Close on link click
  document.querySelectorAll('.mobile-nav-link').forEach(function(a) {
    a.addEventListener('click', closeNav);
  });

  // Escape key
  document.addEventListener('keydown', function(e) {
    if (e.key === 'Escape') closeNav();
  });

  document.querySelectorAll('.quick-add').forEach(function(b) {
    b.addEventListener('click', function(e) {
      e.stopPropagation();
      var old = b.textContent;
      b.textContent = '✓ Agregado';
      b.style.background = 'var(--celeste)';
      b.style.color = 'var(--bg)';
      b.style.borderColor = 'var(--celeste)';
      setTimeout(function() { b.textContent = old; b.style = ''; }, 1400);
      var badge = document.querySelector('.cart-badge');
      if (badge) badge.textContent = (parseInt(badge.textContent || '0') + 1);
    });
  });
</script>
